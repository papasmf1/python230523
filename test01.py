import math

# Basic math operations
x = 4.3
y = 2.7

print("x =", x)
print("y =", y)
print("")

print("Mathematical Operations:")
print("Addition (x + y) =", x + y)
print("Subtraction (x - y) =", x - y)
print("Multiplication (x * y) =", x * y)
print("Division (x / y) =", x / y)
print("")

# Trigonometric functions
angle = math.pi / 4  # 45 degrees

print("Trigonometric Functions:")
print("Sine of", angle, "=", math.sin(angle))
print("Cosine of", angle, "=", math.cos(angle))
print("Tangent of", angle, "=", math.tan(angle))
print("")

# Exponential and logarithmic functions
num = 2.5

print("Exponential and Logarithmic Functions:")
print("e^", num, "=", math.exp(num))
print("Natural logarithm of", num, "=", math.log(num))
print("Base-10 logarithm of", num, "=", math.log10(num))
print("")

# Power and square root
base = 3
exponent = 4

print("Power and Square Root:")
print(base, "raised to the power of", exponent, "=", math.pow(base, exponent))
print("Square root of", base, "=", math.sqrt(base))
